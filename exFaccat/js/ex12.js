// Descrição   : Solicite a temperatura em Fahrenheit e converte em Graus Celsius

alert("Conversor de temperatura de Fahrenheit para Graus Celsius")
let fahrenheit = Number(prompt("Escreva a temperatura em Fahrenheit:"));

let temperaturaConvertida = (5*(fahrenheit-32))/9

alert("A temperatura convertida é igual a: "+ temperaturaConvertida.toFixed(2) +" °C")





